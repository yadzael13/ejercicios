def filter_str_or(param):
    if isinstance(param, str) or isinstance(param, bool) or isinstance(param, int) or isinstance(param, float):
        return 1
    elif isinstance(param, dict):
        return 2
    elif isinstance(param, list):
        return 3



def function_def(my_new, body):
    for el in my_new:
        if el in body:
            if type(my_new[el]) == type(body[el]):
                validate = filter_str_or(my_new[el])
                if  validate == 1:
                    body[el] = my_new[el]

                elif validate == 2:
                    for y in my_new[el]:
                        body[el][y] = my_new[el][y]
                elif validate == 3:
                    try:
                        for y in my_new[el]:
                            for w in y:
                                body[el][my_new[el].index(y)][w] = my_new[el][my_new[el].index(y)][w]
                    except:
                        for y in my_new[el]:
                            if y in body[el]:
                                continue
                            else:
                                if y not in body[el]:
                                    if len(y) == len(body[el][0]):
                                        body[el].append(y)
                                    else:
                                        for w in y:
                                            if w in body[el][0]:
                                                body[el][body[el].index(y)][w] = w
                                            else:
                                                continue


            else:
                print(f"{el}, is not the same type of in given body")
    return body
